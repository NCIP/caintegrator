package gov.nih.nci.caintegrator2.application.study;


import java.util.HashMap;
import java.util.Map;

/**
 * Possible Image type values for <code>StudyLogo.fileType</code>.
 */
public enum ImageContentTypeEnum {

    /**
     * JPEG type.
     */
    JPEG("image/jpeg"),

    /**
     * GIF type.
     */
    GIF("image/gif");
    
    private static Map<String, ImageContentTypeEnum> valueToTypeMap = new HashMap<String, ImageContentTypeEnum>();

    private String value;
    
    private ImageContentTypeEnum(String value) {
        this.value = value;
    }

    /**
     * @return the value
     */
    public String getValue() {
        return value;
    }

    /**
     * @param value the value to set
     */
    public void setValue(String value) {
        this.value = value;
    }

    private static Map<String, ImageContentTypeEnum> getValueToTypeMap() {
        if (valueToTypeMap.isEmpty()) {
            for (ImageContentTypeEnum type : values()) {
                valueToTypeMap.put(type.getValue(), type);
            }
        }
        return valueToTypeMap;
    }
    
    /**
     * Returns the <code>ImageContentTypeEnum</code> corresponding to the given value. Returns null
     * for null value.
     * 
     * @param value the value to match
     * @return the matching type.
     */
    public static ImageContentTypeEnum getByValue(String value) {
        checkType(value);
        return getValueToTypeMap().get(value);
    }

    /**
     * Checks to see that the value given is a legal <code>AssayType</code> value.
     * 
     * @param value the value to check;
     * @return T/F value depending on if is a valid type.
     */
    public static boolean checkType(String value) {
        if (value != null && !getValueToTypeMap().containsKey(value)) {
            return false;
        }
        return true;
    }
}
